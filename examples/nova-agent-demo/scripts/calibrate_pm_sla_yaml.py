def main() -> int:
    if len(sys.argv) < 2:
        print("Usage: calibrate_pm_sla_yaml.py <nodes.json>|embedded", file=sys.stderr)
        return 1
    arg = sys.argv[1]
    if arg == "embedded":
        bands = {}
        for code, (mn, mx) in _EMBEDDED_MIN_MAX.items():
            b = band_for(mn, mx)
            if b:
                bands[code] = b
        sys.stdout.write(emit_yaml(bands))
        return 0
    path = Path(arg)
    nodes = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(nodes, list):
        nodes = [nodes]
    vals = collect_values(nodes)
    bands = {}
    for code, vs in vals.items():
        b = band_for(min(vs), max(vs))
        if b:
            bands[code] = b
    sys.stdout.write(emit_yaml(bands))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
